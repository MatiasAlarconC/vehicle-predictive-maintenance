  static String buildUrl({
    required String make,
    required String model,
    required int year,
  }) {
    final makeSlug = _makeSlug(make);
    final modelSlug = _modelSlug(make, model);
    return '$_baseUrl'
        '?customer=$_customer'
        '&make=$makeSlug'
        '&modelFamily=$modelSlug'
        '&modelYear=$year'
        '&angle=29'
        '&zoomType=fullscreen';
  }

