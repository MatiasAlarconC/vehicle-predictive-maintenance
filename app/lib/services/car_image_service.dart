  static String _modelSlug(String make, String model) {
    final override = _modelOverrides[make]?[model];
    if (override != null) return override;
    return model.toLowerCase().replaceAll(' ', '-').replaceAll('/', '-');
  }

  /// Returns a CDN URL using the first known model for [make] as a brand fallback.
  /// Returns null if the make is completely unknown (e.g. custom make).
  static String? buildBrandFallbackUrl({required String make, required int year}) {
    final models = _modelOverrides[make];
    if (models == null || models.isEmpty) return null;
    final firstSlug = models.values.first;
    final makeSlug = _makeSlug(make);
    return '$_baseUrl'
        '?customer=$_customer'
        '&make=$makeSlug'
        '&modelFamily=$firstSlug'
        '&modelYear=$year'
        '&angle=29'
        '&zoomType=fullscreen';
  }
